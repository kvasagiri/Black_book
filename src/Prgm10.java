/* WAP to display stars in a triangular form - a single star in the first line,
two stars in the second line, and so on */

public class Prgm10 
{
	public static void main (String args[])
	{
		int i=0, j=0;
		for(i=0;i<5;i++)
		{
			for(j=0;j<i;j++)
			{
				System.out.print("*");
			}
			System.out.println(" ");
			System.out.println("Manish");
		}
	}
}
